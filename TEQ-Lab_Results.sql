-- data model
create table patient (
    patient_id         number generated by default as identity primary key,
    patient_mrn        varchar2(30) not null unique,
    patient_name       varchar2(200) not null,
    date_of_birth      date,
    phone_number       varchar2(30),
    email              varchar2(200),
    created_at         timestamp default systimestamp not null
);

create table provider (
    provider_id        number generated by default as identity primary key,
    provider_name      varchar2(200) not null,
    specialty          varchar2(100),
    phone_number       varchar2(30),
    email              varchar2(200),
    created_at         timestamp default systimestamp not null
);

create table lab_order (
    order_id              number generated by default as identity primary key,
    patient_id            number not null,
    ordering_provider_id  number not null,
    order_code            varchar2(50) not null,
    order_name            varchar2(200) not null,
    ordered_at            timestamp default systimestamp not null,
    order_status          varchar2(30) default 'ordered' not null,
    constraint fk_lab_order_patient
        foreign key (patient_id) references patient(patient_id),
    constraint fk_lab_order_provider
        foreign key (ordering_provider_id) references provider(provider_id),
    constraint ck_lab_order_status
        check (order_status in ('ordered','resulted','closed'))
);

create table lab_test_rule (
    test_code                    varchar2(50) primary key,
    test_name                    varchar2(200) not null,
    release_policy               varchar2(30) not null,
    create_followup_on_abnormal  char(1) default 'y' not null,
    escalate_on_critical         char(1) default 'y' not null,
    default_followup_type        varchar2(50) default 'provider_review' not null,
    constraint ck_release_policy
        check (release_policy in ('always','normal_only','after_provider_review')),
    constraint ck_followup_abnormal
        check (create_followup_on_abnormal in ('y','n')),
    constraint ck_escalate_critical
        check (escalate_on_critical in ('y','n'))
);

create table lab_result (
    result_id             number generated by default as identity primary key,
    order_id              number not null,
    test_code             varchar2(50) not null,
    test_name             varchar2(200) not null,
    result_value_num      number,
    result_value_text     varchar2(4000),
    units                 varchar2(50),
    reference_low         number,
    reference_high        number,
    abnormal_flag         char(1) default 'n' not null,
    critical_flag         char(1) default 'n' not null,
    result_status         varchar2(30) default 'received' not null,
    route_reason          varchar2(4000),
    resulted_at           timestamp default systimestamp not null,
    routed_at             timestamp,
    created_at            timestamp default systimestamp not null,
    updated_at            timestamp default systimestamp not null,
    constraint fk_lab_result_order
        foreign key (order_id) references lab_order(order_id),
    constraint fk_lab_result_rule
        foreign key (test_code) references lab_test_rule(test_code),
    constraint ck_lab_result_abnormal
        check (abnormal_flag in ('y','n')),
    constraint ck_lab_result_critical
        check (critical_flag in ('y','n')),
    constraint ck_lab_result_status
        check (result_status in ('received','routed','released','provider_review','failed'))
);

create table result_notification (
    notification_id       number generated by default as identity primary key,
    result_id             number not null,
    recipient_type        varchar2(30) not null,
    recipient_id          number,
    channel               varchar2(30) not null,
    notification_status   varchar2(30) default 'pending' not null,
    message_text          varchar2(4000) not null,
    created_at            timestamp default systimestamp not null,
    sent_at               timestamp,
    constraint fk_result_notification_result
        foreign key (result_id) references lab_result(result_id),
    constraint ck_result_notification_recipient
        check (recipient_type in ('provider','patient')),
    constraint ck_result_notification_channel
        check (channel in ('inbox','portal','sms','email')),
    constraint ck_result_notification_status
        check (notification_status in ('pending','sent','failed'))
);

create table follow_up_task (
    task_id               number generated by default as identity primary key,
    result_id             number not null,
    patient_id            number not null,
    provider_id           number not null,
    task_type             varchar2(50) not null,
    priority              varchar2(20) not null,
    task_status           varchar2(30) default 'open' not null,
    due_at                timestamp,
    task_notes            varchar2(4000),
    created_at            timestamp default systimestamp not null,
    updated_at            timestamp default systimestamp not null,
    constraint fk_follow_up_task_result
        foreign key (result_id) references lab_result(result_id),
    constraint fk_follow_up_task_patient
        foreign key (patient_id) references patient(patient_id),
    constraint fk_follow_up_task_provider
        foreign key (provider_id) references provider(provider_id),
    constraint ck_follow_up_task_priority
        check (priority in ('low','medium','high','critical')),
    constraint ck_follow_up_task_status
        check (task_status in ('open','in_progress','done','cancelled'))
);

create index ix_lab_result_status
    on lab_result (result_status, resulted_at);

create index ix_lab_result_order
    on lab_result (order_id, test_code);

create index ix_notification_result
    on result_notification (result_id, notification_status);

create index ix_followup_result
    on follow_up_task (result_id, task_status);

--04/22 adding a tracking mechanism to see workflow 
create table lab_result_event (
    event_id        number generated by default as identity primary key,
    result_id       number not null,
    stage_code      varchar2(50) not null,
    stage_label     varchar2(200) not null,
    stage_order     number not null,
    details         varchar2(4000),
    created_at      timestamp default systimestamp not null,
    constraint fk_lab_result_event_result
        foreign key (result_id) references lab_result(result_id)
);

create index ix_lab_result_event_result
    on lab_result_event (result_id, stage_order, created_at);

